    private void lblGuardarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblGuardarMouseClicked
        // TODO add your handling code here:
        int id = 1;
        try {

            while (manager.getActividadDAO().obtener(id) != null) {
                id++;
            }

        } catch (DAOException ex) {
            Logger.getLogger(Perfil.class.getName()).log(Level.SEVERE, null, ex);
        }
        DateTimeFormatter date1 = DateTimeFormatter.ofPattern("dd/MM/yyyy");
        LocalDate fechaactual = LocalDate.now();
        String fecha = fechaactual.format(date1);
        act = new Actividad(id, usuario.getCodigo(), 0, "Realizado", "Actualizar", fecha);
        saveData();
        try {
            manager.getActividadDAO().insertar(act);
            manager.getUsuarioDAO().modificar(usuario);
            JOptionPane.showMessageDialog(null, "Se ha actualizado su información", "Sistema", JOptionPane.INFORMATION_MESSAGE);
        } catch (DAOException ex) {
            Logger.getLogger(Perfil.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_lblGuardarMouseClicked

    private void lblEliminarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblEliminarMouseClicked
        try {
            // TODO add your handling code here:
            int id = 1;
            try {

                while (manager.getActividadDAO().obtener(id) != null) {
                    id++;
                }

            } catch (DAOException ex) {
                Logger.getLogger(Perfil.class.getName()).log(Level.SEVERE, null, ex);
            }
            DateTimeFormatter date1 = DateTimeFormatter.ofPattern("dd/MM/yyyy");
            LocalDate fechaactual = LocalDate.now();
            String fecha = fechaactual.format(date1);
            int n = JOptionPane.showConfirmDialog(null, "¿Esta seguro de eliminar sus datos?", "Mensaje del Sistema",
                    JOptionPane.OK_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE);
            if (n == JOptionPane.OK_OPTION) {
                DetalleFactura detfac = manager.getDetalleFacturaDAO().obtener(usuario.getCodigo());
                manager.getLoginDAO().eliminar(login);
                manager.getDetalleFacturaDAO().eliminar(detfac);
                manager.getUsuarioDAO().eliminar(usuario);

                ventana2.dispose();
                jFrmPrincipal ventana = new jFrmPrincipal();
                ventana.setVisible(true);
                ventana.pack();
            } else if (n == JOptionPane.CANCEL_OPTION) {
                act = new Actividad(id, usuario.getCodigo(), 0, "No Realizado", "Eliminar", fecha);
                manager.getActividadDAO().insertar(act);
            }
        } catch (DAOException ex) {
            Logger.getLogger(Perfil.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_lblEliminarMouseClicked


