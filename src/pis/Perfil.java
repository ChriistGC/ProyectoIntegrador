    public void loadData() {
        if (usuario != null) {
            txtCodigo.setText(usuario.getCodigo() + "");
            lblNomCom.setText(usuario.getNombres() + " " + usuario.getApellidos());
            txtCedula.setText(usuario.getCedula());
            txtTelefono.setText(usuario.getTelefono());
            txtDireccion.setText(usuario.getDireccion());
            txtCorreo.setText(usuario.getCorreo());
            txtSexo.setText(usuario.getSexo());
            txtCiudad.setText(usuario.getCiudad());
            lblUser.setText(login.getUsuario());
            pswTxtContrasena.setText(login.getContraseña());

        } else {
            txtCodigo.setText("");
            lblNomCom.setText("");
            txtCedula.setText("");
            txtTelefono.setText("");
            txtDireccion.setText("");
            txtCorreo.setText("");
            txtSexo.setText("");
            txtCiudad.setText("");
            lblUser.setText("");
            pswTxtContrasena.setText("");
        }

        txtCodigo.requestFocus();
    }

